package org.study.chapter02;

public class FloatDoubleExer {
    public static void main(String[] args) {
        double PI = 3.14;
        double radius1 = 1.2;
        double radius2 = 2.5;
        int radius3 = 6;
        System.out.println(PI * radius1 * radius1);
        System.out.println(PI * radius2 * radius2);
        System.out.println(PI * radius3 * radius3);

    }
}
